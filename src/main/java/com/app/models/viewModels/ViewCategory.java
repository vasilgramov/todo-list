package com.app.models.viewModels;

public class ViewCategory {

    public long id;

    private String name;

    public ViewCategory() {
        super();
    }

    public long getId() {
        return this.id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
